; Note you can use any address between $8000 and $9FFF for MMC1CTRL
MMC1CTRL	.equ $8000
; Note you can use any address between $A000 and $BFFF for MMC1CHR0
MMC1CHR0	.equ $A000
; Note you can use any address between $C000 and $DFFF for MMC1CHR1
MMC1CHR1	.equ $C000
; Note you can use any address between $E000 and $FFFF for MMC1PRG
MMC1PRG		.equ $E000
